
package jo8;


public class Contoh01 {

   
    public static void main(String[] args) {
      int x; 
      for(x = 1; x <= 5; x++)
      {
          System.out.println("SMK Telkom Malang");
      }
      
      int i = 1;
      while (i <= 5)
      {
            System.out.println("SMK Telkom Malang");
            i++;
      }
      
      int j = 1;
      do
      {
            System.out.println("SMK Telkom Malang");
            j++;
      }
      while(j <= 5);
    }
    
}
